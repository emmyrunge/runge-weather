package runge.weather;

public class Wind
{
    private double speed;
    private double deg;

    public double getSpeed() {
        return speed;
    }

    public void setSpeed(int input) {
        this.speed = input;
    }

    public double getDeg() {
        return deg;
    }

    public void setDeg(int input) {
        this.deg = input;
    }
}
